			if (race ~= nil and not __bit_check_race(race)) or (class ~= nil and not __bit_check_class(class)) then
				tremove(avl_quest_list, index);
			else
				avl_quest_hash[quest] = 1;
			end
		else
			tremove(avl_quest_list, index);
		end
	end
	for quest, info in next, __db_quest do
		local _next = info.next;
		if _next ~= nil then
			chain_prev_quest[_next] = quest;
		end
	end
	for item, list in next, item_related_quest do
		for index = #list, 1, -1 do
			if blacklist_quest[quest] ~= nil then
				tremove(list, index);
			end
		end
	end
end

